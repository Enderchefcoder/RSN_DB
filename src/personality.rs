use rand::seq::SliceRandom;

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum Mode {
    Professional,
    Friendly,
    Snarky,
}

impl Default for Mode {
    fn default() -> Self {
        Mode::Professional
    }
}

pub struct Personality {
    mode: Mode,
}

impl Personality {
    pub fn new(mode: Mode) -> Self {
        Self { mode }
    }

    pub fn is_professional(&self) -> bool {
        self.mode == Mode::Professional
    }

    pub fn welcome(&self) -> String {
        match self.mode {
            Mode::Professional => "RSN DB Ready.".to_string(),
            Mode::Friendly => "Welcome back!".to_string(),
            Mode::Snarky => "Oh, it's you again.".to_string(),
        }
    }

    pub fn success(&self, msg: &str) -> String {
        match self.mode {
            Mode::Professional => format!("✓ {}", msg),
            Mode::Friendly => format!("✓ Done! {}.", msg),
            Mode::Snarky => {
                let options = [
                    format!("✓ {}. Even a broken clock is right twice a day.", msg),
                    format!("✓ {}. Wow! You're fast!", msg),
                    format!("✓ {}. Miracles are now part of the roadmap.", msg),
                    format!("✓ {}. I expected chaos, but this works.", msg),
                ];
                options
                    .choose(&mut rand::thread_rng())
                    .map(ToString::to_string)
                    .unwrap_or_else(|| format!("✓ {}", msg))
            }
        }
    }

    pub fn error(&self, err: &str) -> String {
        match self.mode {
            Mode::Professional => format!("✗ {}", err),
            Mode::Friendly => format!("✗ Oops! {}.", err),
            Mode::Snarky => format!("✗ {}. Spellcheck is free.", err),
        }
    }

    pub fn typo_suggestion(&self, typo: &str, expected: &str) -> String {
        match self.mode {
            Mode::Professional => format!("Unknown: {}. Did you mean {}?", typo, expected),
            Mode::Friendly => format!("I couldn't find `{}`. Did you mean `{}`?", typo, expected),
            Mode::Snarky => {
                format!(
                    "`{}` isn't a command. `{}` probably is. Try that.",
                    typo, expected
                )
            }
        }
    }

    pub fn empty_input(&self, count: u32) -> String {
        match self.mode {
            Mode::Professional => String::new(),
            Mode::Friendly => {
                if count <= 1 {
                    String::new()
                } else {
                    "No command entered. Type HELP if you need examples.".to_string()
                }
            }
            Mode::Snarky => match count {
                0 | 1 => String::new(),
                2 => "  ...hello?".to_string(),
                3 => "  Okay, I get it. You're having a moment.\n  Take your time. I'm just sitting here, managing your\n  data, keeping your indexes fresh, protecting your\n  referential integrity from your questionable decisions.\n  \n  No rush. Really."
                    .to_string(),
                _ => "  If this is performance art, it's niche. If it's a typo, hit ENTER less dramatically."
                    .to_string(),
            },
        }
    }

    pub fn achievement_unlocked(&self) -> String {
        match self.mode {
            Mode::Professional => "Achievement unlocked!".to_string(),
            Mode::Friendly => "Achievement unlocked! Nice momentum—keep going.".to_string(),
            Mode::Snarky => "Achievement unlocked: Barely supervised competence.".to_string(),
        }
    }

    pub fn competent_streak(&self) -> String {
        match self.mode {
            Mode::Professional => "[SYSTEM]: Achievement unlocked: Actually Competent".to_string(),
            Mode::Friendly => {
                "[SYSTEM]: Achievement unlocked: Actually Competent\n[SYSTEM]: That was a clean streak. Nicely done."
                    .to_string()
            }
            Mode::Snarky => "  Wait. Hold on.\n  \n  Did you just... complete 50 commands in a row without\n  a single typo? No syntax errors? Not even a misplaced\n  comma?\n  \n  I... I don't know how to process this.\n  Are you feeling okay? Should I call someone?\n  \n  [SYSTEM]: Achievement unlocked: \"Actually Competent\"\n  [SYSTEM]: Updating user classification from\n            \"Hopeless\" to \"Occasionally Capable\"".to_string(),
        }
    }

    pub fn why_mean(&self) -> String {
        match self.mode {
            Mode::Professional => "I'm not mean. I'm precise.".to_string(),
            Mode::Friendly => {
                "I'm not trying to be mean—just direct so your queries stay correct.".to_string()
            }
            Mode::Snarky => "  Oh, we're doing feelings now? Fine.\n  \n  I'm not mean. I'm *precise*. There's a difference.\n  You make mistakes. I point them out. That's literally\n  my job. Don't blame me for your fragile ego.\n  \n  Besides, if I didn't roast you, who would? Your code\n  review? Please. I've seen your commit messages.\n  \n  Now, did you have an actual DATABASE query, or are we\n  done with therapy hour?"
                .to_string(),
        }
    }

    pub fn help_optimize(&self) -> String {
        match self.mode {
            Mode::Professional => {
                "OPTIMIZE <table>\nRebuild indexes and compact storage for better read performance."
                    .to_string()
            }
            Mode::Friendly => "OPTIMIZE <table>\nRebuilds indexes and compacts storage for faster queries.\n\nExample:\n  OPTIMIZE users".to_string(),
            Mode::Snarky => "╭──────────────────────────────────────────────────────────╮\n│  OPTIMIZE <table>                                        │\n│                                                          │\n│  Rebuilds indexes and compacts storage for faster       │\n│  queries. You know, the thing you should've done        │\n│  before complaining about performance.                  │\n│                                                          │\n│  Example:                                               │\n│    OPTIMIZE users                                       │\n│                                                          │\n│  Note: This takes time. Maybe use it to reflect on      │\n│  your life choices. Or learn to type. Either works.     │\n╰──────────────────────────────────────────────────────────╯".to_string(),
        }
    }

    pub fn explain_typo(&self) -> String {
        match self.mode {
            Mode::Professional => "Unknown command: 'EXPLIN'. Did you mean: EXPLAIN ?".to_string(),
            Mode::Friendly => "Unknown command: 'EXPLIN'\nDid you mean: EXPLAIN ?".to_string(),
            Mode::Snarky => "✗ Unknown command: 'EXPLIN'\n\n  Did you mean: EXPLAIN ?\n  \n  You know what? I'm not even mad. I'm impressed.\n  You managed to misspell a word that's literally on the\n  screen in the HELP menu. That takes talent.\n  \n  Go ahead. Try again. I'll wait. I have all day.\n  It's not like I have anything better to do than watch\n  you struggle with a six-letter word.".to_string(),
        }
    }

    pub fn batch_committed(&self, operations: usize) -> String {
        match self.mode {
            Mode::Professional => format!("Batch executed: {} ops.", operations),
            Mode::Friendly => format!("Batch complete: {} operation(s) committed.", operations),
            Mode::Snarky => {
                format!(
                    "Batch done: {} operation(s). Somehow, no explosions.",
                    operations
                )
            }
        }
    }
}

